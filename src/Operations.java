public class Operations {

    static int sum (int x, int y) {
        int z = x + y;
        return z;
    }

    static int sub (int x, int y) {
        int z = x - y;
        return z;
    }

    static int mul (int x, int y) {
        int z = x * y;
        return z;
    }

    static int div (int x, int y) {
        int z = x / y;
        return z;
    }
}
